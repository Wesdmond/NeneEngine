NeneApp::NeneApp(HINSTANCE mhAppInst, HWND mhMainWnd) : DX12App(mhAppInst, mhMainWnd) {}

bool NeneApp::Initialize()
{
    if (!DX12App::Initialize())
        return false;
    if (!LoadAssets())
        return false;
    
    return true;
}

